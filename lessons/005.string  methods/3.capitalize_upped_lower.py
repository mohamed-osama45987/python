# you can use the capitalize to capitalize the first letter of the first word 

name = "mohamed OsaMa"

capitalize  = name.capitalize()

print(capitalize) # Mohamed osama


# you can use upper to capitalize every single letter not just the intial letter

upper_cased= name.upper()

print(upper_cased) # MOHAMED OSAMA

# here is lower ()

lower_cased =  name.lower()

print(lower_cased)
