# we can use the built in math class to get access to useful functions
import math

print(math.pi) # gives you the value of pi 3.14159...
print (math.e) # gives you the value of e expontal constat 2.718...

# math.sqrt()
x = 9
result = math.sqrt(x)
print(result) # gives you the square root of 9 which will be 3.0

# math.ceil () rounds number up
x = 10.1
result = math.ceil(x)
print(result) # 11

# math.floor () rounds number down
x = 9.9 
result = math.floor(x)
print(result) # 9
