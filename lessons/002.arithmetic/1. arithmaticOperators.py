# we cover the basic operators like + - / * ^ % 
friends = 10 

friends = friends + 1

friends += 1 # same as above but better syntax called addition assigment operator


print(f"Value of friends after plus {friends}")

# minus ( - )
friends = friends - 1 # yes you can use -= instead

print(f"Value of friends after minus {friends}")

# multiply ( * )
friends = friends * 2 # you can use *=

print(f"Value of friends after multiply {friends}")

#  division ( / )

friends = friends / 2

print(f"Value of friends after division {friends}")

#  exponent ( ** ) power of

friends = friends ** 2 # you can use **=

print(f"Value of friends after exponent {friends}")

# modulus ( % ) gives you the remainder after division

friends = friends % 2

print(f"Value of friends after modulus {friends}")

